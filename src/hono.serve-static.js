// src/middleware/serve-static/serve-static.ts
var DEFAULT_DOCUMENT = "index.html";
var serveStatic = (opt = { root: "" }) => {
  return async (c, next) => {
    await next();
    const url = new URL(c.req.url);
    const path = getKVFilePath({ filename: url.pathname, root: opt.root, defaultDocument: DEFAULT_DOCUMENT });
    const content = await getContentFromKVAsset(path);
    if (content) {
      const mimeType = getMimeType(path);
      if (mimeType) {
        c.header("Content-Type", mimeType);
      }
      c.res = c.body(content);
    } else {
    }
  };
};
export {
  serveStatic
};
